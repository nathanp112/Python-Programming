# two strings together
string1 = "Nathan"
string2 = "Prince"
print("Strings:", string1 + " " + string2)

# two integers together
int1 = 7
int2 = 4
print("Integers:", int1 + int2)

# two float values
float1 = 6.78
float2 = 7.56
print("Floats:", float1 + float2)

# two booleans
bool1 = True
bool2 = False
print("Bools:", bool1 + bool2)
